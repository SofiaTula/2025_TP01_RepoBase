// Función para sumar dos números
function sumar(a, b) {
    return a + b;
}

function resta(a, b) {
    return a - b;
}

// Ejemplo de uso
let numero1 = 8;
let numero2 = 2;

console.log("La suma es: " + sumar(numero1, numero2));
console.log("La resta es: " - resta(numero1, numero2));

saludar();
